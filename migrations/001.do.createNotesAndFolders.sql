CREATE TABLE folders (
	id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
	name TEXT NOT NULL,
	modified TIMESTAMP DEFAULT now() NOT NULL,
);

CREATE TABLE notes (
	id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
	modified TIMESTAMP DEFAULT now() NOT NULL,
	content TEXT,
	name TEXT NOT NULL,
  CONSTRAINT folder_id
    FOREIGN KEY (folder_id)
    REFERENCES folders (id)
    ON DELETE CASCADE
);